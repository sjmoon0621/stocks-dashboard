/* marketCap 표기 헬퍼: 십억달러($B) → 사람이 읽는 문자열 */
function fmtMarketCap(b) {
  if (b == null || isNaN(b)) return "—";
  if (b >= 1000) return "$" + (b / 1000).toFixed(2).replace(/\.?0+$/, "") + "조";
  return "$" + b.toFixed(0) + "B";
}
